# Tipe Data

# tipe data Yang tidak Ada koma

a = 10

print("data :", a)

print("type: ", type(a))

# tipe data : angka dengan koma (float)

data_float = 1.5

print("ini data ", data_float)

print("Type:", type(data_float))

# tipe data string

a = "Aku"

print("data :", a)

print("type :", type(a))

# type data Binner true/false (bollean)

data_bollean = True

print("Data ", data_bollean)

print("Type :", type(data_bollean))

